package Aula6;

public class CountFrom1to10 {

	public static void main(String[] args) {
		// Inicializa o contador do loop
		int contador = 1;
		// Loop do while
		while (contador <= 10) {
			System.out.println(contador); // Imprime o número atual
			contador++; // Adiciona +1 no contador
		}
	}
}